package com.example.lab11.model;

public class MyChat {
    private int friend_id;
    private String friend_image;
    private String friend_name;
    private String friend_phone_number;
    private String friend_dob;
    private String friend_address;
    private String friend_stdid;


    public int getFriend_id() {
        return friend_id;
    }

    public void setFriend_id(int friend_id) {
        this.friend_id = friend_id;
    }

    public String getFriend_image() {
        return friend_image;
    }

    public void setFriend_image(String friend_image) {
        this.friend_image = friend_image;
    }

    public String getFriend_name() {
        return friend_name;
    }

    public void setFriend_name(String friend_name) {
        this.friend_name = friend_name;
    }

    public String getFriend_phone_number() {
        return friend_phone_number;
    }

    public void setFriend_phone_number(String friend_phone_number) {this.friend_phone_number = friend_phone_number;}

    public String getFriend_dob() {
        return friend_dob;
    }

    public void setFriend_dob(String friend_dob) {this.friend_dob = friend_dob;}

    public String getFriend_address() {
        return friend_address;
    }

    public void setFriend_address(String friend_address) {
        this.friend_address = friend_address;
    }

    public String getFriend_stdid() {
        return friend_stdid;
    }

    public void setFriend_stdid(String friend_stdid) {
        this.friend_stdid = friend_stdid;
    }
}
